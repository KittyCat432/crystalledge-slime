using Content.Client.Message;
using Content.Shared._CE.Skill.Core.Prototypes;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;

namespace Content.Client._CE.SkillsUpgradeable.UI;

[GenerateTypedNameReferences]
public sealed partial class CESkillUpgradeEntry : PanelContainer
{
    private ProtoId<CESkillPrototype> SkillId { get; set; }

    public event Action<ProtoId<CESkillPrototype>>? OnSkillSelected;

    public CESkillUpgradeEntry()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        SelectButton.OnPressed += _ => OnSkillSelected?.Invoke(SkillId);
    }

    public void SetData(
        ProtoId<CESkillPrototype> skillId,
        string skillType,
        string name,
        string description,
        Texture? icon)
    {
        SkillId = skillId;
        SkillType.SetMarkup(skillType);
        SkillName.SetMarkup(name);
        SkillDesc.SetMarkup(description);

        if (icon != null)
            SkillIcon.Texture = icon;
    }
}
